export function getCalloutsForStep(
  step: string,
  platform: string | null,
  source: string | null,
): Callout[] {
  return callouts.filter((c) => {
    if (!c.steps.includes(step as any)) return false;
    if (c.platforms.length > 0) {
      if (!platform || !c.platforms.includes(platform as any)) return false;
    }
    if (c.sources.length > 0) {
      if (!source || !c.sources.includes(source as any)) return false;
    }
    return true;
  });
}
